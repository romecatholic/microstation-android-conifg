package com.dgm.bean;

public class StationEquip {
	private String equipName;
	private String equipStationName;
	private String equipArea;
	private String deviceId;
	private String equipment_id;
	

	public String getDeviceId() {
		return deviceId;
	}

	public void setDeviceId(String deviceId) {
		this.deviceId = deviceId;
	}

	public String getEquipment_id() {
		return equipment_id;
	}

	public void setEquipment_id(String equipment_id) {
		this.equipment_id = equipment_id;
	}

	public String getEquipName() {
		return equipName;
	}

	public void setEquipName(String equipName) {
		this.equipName = equipName;
	}

	public String getEquipStationName() {
		return equipStationName;
	}

	public void setEquipStationName(String equipStationName) {
		this.equipStationName = equipStationName;
	}

	public String getEquipArea() {
		return equipArea;
	}

	public void setEquipArea(String equipArea) {
		this.equipArea = equipArea;
	}

}
